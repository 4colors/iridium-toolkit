from itertools import izip
def grouped(iterable, n):
    "s -> (s0,s1,s2,...sn-1), (sn,sn+1,sn+2,...s2n-1), (s2n,s2n+1,s2n+2,...s3n-1), ..."
    return izip(*[iter(iterable)]*n)

def ascii_to_binary(s):
    return ''.join(["{0:07b}".format(ord(c)) for c in s])

